#!/usr/bin/env node
/**
 * dump-split-matrix.mjs
 *
 * Calls generateProgram for every (splitType, daysPerWeek) combination
 * currently listed as valid in SplitSheet (plus the boundary cases we
 * newly support) and writes a Markdown matrix to `../split-day-matrix.md`
 * in the parent repo.
 *
 * Run from foundry-app/:
 *   node scripts/dump-split-matrix.mjs
 *
 * The matrix is committed to the parent repo so reviewers can eyeball
 * label + tag coverage without spinning up a test run.
 *
 * We strip TS types from `program.ts` via esbuild on the fly so we can
 * import it without a precompiled dist/ — keeps this script a single
 * `node scripts/...` invocation.
 */
import { mkdtempSync, writeFileSync, readFileSync } from 'node:fs';
import { tmpdir } from 'node:os';
import { fileURLToPath, pathToFileURL } from 'node:url';
import { dirname, resolve, join } from 'node:path';
import esbuild from 'esbuild';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);

// Build program.ts + its single ts dep (training.ts) to a temp ESM bundle
// that only uses node-compatible syntax.
const tmp = mkdtempSync(join(tmpdir(), 'foundry-dump-'));
const bundleOut = join(tmp, 'program.mjs');

await esbuild.build({
  entryPoints: [resolve(__dirname, '../src/utils/program.ts')],
  outfile: bundleOut,
  bundle: true,
  format: 'esm',
  platform: 'node',
  target: 'node18',
  logLevel: 'error',
  // program.ts → training.ts → sync.ts → supabase.ts reads import.meta.env.
  // Stub it so the module graph loads without a real Vite build.
  define: {
    'import.meta.env.VITE_SUPABASE_URL': JSON.stringify('http://stub.local'),
    'import.meta.env.VITE_SUPABASE_ANON_KEY': JSON.stringify('stub-key'),
    'import.meta.env.DEV': 'false',
    'import.meta.env.PROD': 'true',
  },
});

const { generateProgram } = await import(pathToFileURL(bundleOut).href);
const { EXERCISE_DB } = await import(
  pathToFileURL(resolve(__dirname, '../src/data/exercises.js')).href
);

const BASE_PROFILE = {
  experience: 'intermediate',
  equipment: ['barbell', 'dumbbell', 'cable', 'machine', 'bodyweight', 'band', 'kettlebell'],
  sessionDuration: 60,
  goal: 'build_muscle',
};

/** [splitType, [daysPerWeek, ...]] pairs we want to document. */
const MATRIX = [
  ['ppl', [3, 5, 6]],
  ['upper_lower', [2, 3, 4, 5, 6]],
  ['push_pull', [2, 3, 4, 5, 6]],
  ['full_body', [2, 3, 4, 5]],
  ['traditional', [4, 5]],
];

const lines = [];
lines.push('# Split × days matrix');
lines.push('');
lines.push(
  'Auto-generated by `foundry-app/scripts/dump-split-matrix.mjs`. Documents ' +
    'the labels and tags `generateProgram` emits for every supported ' +
    '(splitType, daysPerWeek) pair. Regenerate after touching ' +
    '`src/utils/program.ts`.'
);
lines.push('');

for (const [splitType, daysList] of MATRIX) {
  lines.push(`## ${splitType}`);
  lines.push('');
  lines.push('| days | labels | tags |');
  lines.push('|---|---|---|');
  for (const daysPerWeek of daysList) {
    const days = generateProgram(
      { ...BASE_PROFILE, splitType, daysPerWeek },
      EXERCISE_DB
    );
    const labels = days.map((d) => d.label).join(' · ');
    const tags = days.map((d) => d.tag).join(' · ');
    lines.push(`| ${daysPerWeek} | ${labels} | ${tags} |`);
  }
  lines.push('');
}

const out = resolve(__dirname, '../../split-day-matrix.md');
writeFileSync(out, lines.join('\n') + '\n');
console.log(`Wrote ${out}`);
